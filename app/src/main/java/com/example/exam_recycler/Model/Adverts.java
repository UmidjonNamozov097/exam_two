package com.example.exam_recycler.Model;

public class Adverts {
    private int img;
    private String adv;

    public Adverts(int img, String adv) {
        this.img = img;
        this.adv = adv;
    }

    public int getImg() {
        return img;
    }

    public String getAdv() {
        return adv;
    }
}
